class operador:
    #funciones de operadores
    def sumar(self, a, b):
        resul = a+b
        return resul

    def restar(self, a, b):
        resul = a-b
        return resul

    def multiplicar(self, a, b):
        resul = a*b
        return resul

    def dividir(self, a, b):
        resul = a/b
        return resul